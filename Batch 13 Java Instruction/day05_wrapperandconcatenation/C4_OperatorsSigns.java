package day05_wrapperandconcatenation;

public class C4_OperatorsSigns {

	public static void main(String[] args) {
		
		
		// = atama/assignment iþareti 
		
		int num1=5 ;
		int num2=4 ;
		
		boolean isGreat =num1>num2  ;
		System.out.println(isGreat);
		
	
		boolean isSmal= num1<=num2;
		System.out.println(isSmal);
		
		
		//  >   büyüktür iþareti
		//  <  küçüktür iþareti
		//  >=  büyük veya eþittir iþareti
		//  <=  küçük eþittir iþareti 
		
		boolean isEqual = num1 ==num2; 
		System.out.println(isEqual);
		
		//  == bu iþaret ise bu ikisi eþitmidir sorusudur 
		//  özel adý ise Comparison(karþýlaþtýrma) operator
		
		System.out.println(num1==5); //true
		System.out.println(num1*num2==20);//true
		
		
		System.out.println(num1+num2  !=9); // bu iþaretde eþit deðilmidir sorusunu sorar
		
		//  !=  eþit deðilmidir sorusu iþareti 
		
		
		
		
		

	}

}
